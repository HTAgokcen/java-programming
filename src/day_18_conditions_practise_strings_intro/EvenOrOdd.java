package day_18_conditions_practise_strings_intro;

public class EvenOrOdd {
    public static void main(String[] args) {
        int num = 9;
        if (num % 2 == 0) { //divisible by 2
            System.out.println(num + " is even");
        } else {
            System.out.println(num + " is odd");
        }

    }
}

