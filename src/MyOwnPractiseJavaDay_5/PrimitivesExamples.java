package MyOwnPractiseJavaDay_5;

public class PrimitivesExamples {
    public static void main(String[] args) {
        byte byteValue = 100;
        short shortValue = 32000;
        int intValue = 2134567890;
        long longValue = 358296943759L;
        //***** DECIMAL NUMBERS/FLOATING POINT TYPES *****
        float floatValue = 2345.55F;
        double doubleValue = 2355432.4334; //2355432.4334d also valid
        // ***** CHARACTER TYPE ******
        char charValue = 'A'; //ONLY ONE CHARACTER SINGLE
        //***** BOOLEAN = true |false *****
        boolean booleanValue = true;
        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(booleanValue);

        /*
        Sarah Arslan Ascii table code
        S=1, a=97, r=114, a=97, h=104
        A=6, r=114, s=115, l=108, a=97, n=110
        Sarah-> 1 97 114 97 104
        Arslan-> 6 114 115 108 97 110
         */
    }
}
